export const getPriceIdxFromRange = (rangeStr) => {
  if (!rangeStr) return "r1";
  const match = rangeStr.match(/^([\d.]+)/);
  if (!match) return "r1";
  const weight = parseFloat(match[1]);

  if (weight >= 0.002 && weight < 0.005) return "r1";
  if (weight >= 0.005 && weight < 0.008) return "r2";
  if (weight >= 0.008 && weight < 0.009) return "r3";
  if (weight >= 0.009 && weight < 0.022) return "r4";
  if (weight >= 0.022 && weight < 0.030) return "r5";
  if (weight >= 0.030 && weight < 0.040) return "r6";
  if (weight >= 0.040 && weight < 0.045) return "r7";
  if (weight >= 0.045 && weight < 0.052) return "r8";
  if (weight >= 0.052 && weight < 0.059) return "r9";
  if (weight >= 0.059 && weight < 0.070) return "r10";
  if (weight >= 0.070 && weight < 0.079) return "r11";
  if (weight >= 0.079 && weight < 0.100) return "r12";
  if (weight >= 0.100 && weight < 0.126) return "r13";
  if (weight >= 0.126 && weight < 0.160) return "r13";
  if (weight >= 0.160 && weight < 0.190) return "r14";
  if (weight >= 0.190 && weight < 0.230) return "r15";
  if (weight >= 0.230 && weight < 0.300) return "r16";
  if (weight >= 0.300 && weight < 0.400) return "r17";
  if (weight >= 0.400 && weight < 0.500) return "r18";
  if (weight >= 0.500 && weight < 0.700) return "r19";
  if (weight >= 0.700 && weight < 0.900) return "r20";
  if (weight >= 0.900 && weight < 1.00) return "r21";
  return "r22";
};

